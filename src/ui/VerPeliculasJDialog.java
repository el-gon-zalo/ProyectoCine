    public void actualizarTabla() {
        Pelicula[] listado = pModel.obtenerPeliculas();

        if(listado == null) {
            JOptionPane.showMessageDialog(this, "Error de BD:\n" + pModel.getError());
            close();
        }
        else {
            ((DefaultTableModel)jTable1.getModel()).setRowCount(0);
            int i = 0;

            for(Pelicula peli : listado) {
                Object[] row = new Object[] {
                        peli.getId_pelicula(),
                        peli.getTitulo(),
                        peli.getAnio(),
                        peli.getDuracion(),
                        peli.getGenero()
                };

                ((DefaultTableModel) jTable1.getModel()).insertRow(i, row);
                i++;
            }
        }
    }

