    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Pelicula p = new Pelicula();
        p.setId_pelicula((int)jTable1.getValueAt(jTable1.getSelectedRow(), 0));

        boolean ok = pModel.eliminarPelicula(p);

        if(!ok) {
            JOptionPane.showMessageDialog(this, "Error de BD:\n" + pModel.getError());
        }
        else {
            JOptionPane.showMessageDialog(null, "Pelicula eliminada correctamente (ID = " + p.getId_pelicula() + ". Tï¿½tulo: " + p.getTitulo() + ")");
            actualizarTabla();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

